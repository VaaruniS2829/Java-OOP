class HashCode {
    public static void main(String[] args) {
        String name1 = "Anjali";
        String name2 = "Kavya";
        String name3 = "Sushanth";

        System.out.println("hashCode of name1: " + name1.hashCode());
        System.out.println("hashCode of name2: " + name2.hashCode());
        System.out.println("hashCode of name3: " + name3.hashCode());
    }
}
